        <div class="biography__description"> 
          <p>Максим Николаевич Слепов родился 20 сентября 1973 года в Волгограде.</p>
          <p>В 1997 году окончил Тюменскую государственную медицинскую академию по специальности «Лечебное дело». В 2004 году получил второе высшее образование в Московском государственном социальном университете по специальности «Государственное и муниципальное управление».</p>
          <p>В 2005-2008 годах прошёл обучение в Академии народного хозяйства при Правительстве Российской Федерации по программе подготовки руководящих кадров «Президент», в 2005-2007 годах – по программе MBA «Мастер делового администрирования».</p>
          <p>Начал работать в Сургуте в 1998 году врачом-нефрологом в Окружном кардиологическом диспансере «Центр диагностики и сердечно-сосудистой хирургии». Трудился на руководящих должностях в Центре диагностики и сердечно-сосудистой хирургии, Сургутском филиале Окружного фонда обязательного медицинского страхования. В 2007-2024 годах возглавлял Сургутскую городскую клиническую поликлинику № 1. Имеет высшую квалификационную категорию по специальности «Организация здравоохранения», кандидат медицинских наук.</p>
          <p>В 2011 году был избран депутатом городской Думы, работал в составе Дум города Сургута V, VI и VII созывов. С 2021 по 2024 годы – Председатель Думы города, председатель постоянного комитета Думы города по бюджету, налогам, финансам и имуществу.</p>
          <p>3 июля 2024 года избран Главой города Сургута.</p>
          <p>С 2005 года является членом Всероссийской политической партии «ЕДИНАЯ РОССИЯ».</p>
          <p>Удостоен Нагрудного знака «Отличник здравоохранения», Почётных грамот Министерства здравоохранения РФ, Департамента здравоохранения ХМАО-Югры, ТФОМС Югры. Имеет Почётные грамоты и Благодарственные письма Тюменской областной Думы, Думы Ханты-Мансийского автономного округа – Югры, Думы города Сургута.</p>
          <p>В 2010 и 2013 годах занесён на Доску Почёта города Сургута, в 2017 году награждён Благодарностью Главы города Сургута, в 2022 – знаком «За заслуги перед городом Сургутом».</p>
          <p>Женат, воспитывает четверых детей. </p>
