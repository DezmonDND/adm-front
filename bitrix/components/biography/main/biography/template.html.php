        <div class="biography__description"> 
          <p>Родился 1 июня 1973 года в Курске, в 1988 году после окончания 8-го класса вместе с родителями переехал в Югру. Здесь получил первое образование в <span class="biography__description-span">Нижневартовском нефтяном техникуме</span> по специальности «Бурение нефтяных и газовых скважин». </p>
          <p>В 2004 году закончил Уральскую академию государственной службы по специальности «Государственное и муниципальное управление». Имеет квалификационную степень в сфере управления <span class="biography__description-span">MBA (Master of Business Administration)</span> Российской академии народного хозяйства и государственной службы при Президенте Российской Федерации. </p>
          <p class="biography__description-span"> Кандидат социологических наук. </p>
          <p>До 2012 года занимался предпринимательской деятельностью. </p>
          <p>Начиная с 2006 года дважды избирался депутатом муниципального уровня и дважды – депутатом Думы Югры. </p>
          <p>С 2013 по 2015 год работал заместителем Губернатора ХМАО – Югры. </p>
          <p>Курировал внутриполитический блок. </p>
          <p>С 2016 по 2021 год возглавлял Комитет по бюджету, финансам и налоговой политике Думы Югры. </p>
          <p>27 февраля 2021 года избран Главой города Сургута. </p>
          <p class="biography__description-span">Член партии «ЕДИНАЯ РОССИЯ».</p>
          <p>Женат, воспитывает двоих сыновей. </p>
